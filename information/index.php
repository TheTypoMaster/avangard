<ul><a name="link5"></a><b>5. Конструкция дивана</b> 
  <br />
 Каркас изделия жесткий, неразборный, решетчато-щитовой конструкции. Щитовые детали выполнены из калиброванной фанеры. Брусковые детали изготовлены из древесины хвойных и твердолистных пород &ndash; кавказский бук. Бельевой ящик в оттоманке выполнен из ДСП. 
  <br />
 В основании диван-кровати, кресла-кровати, кресла, оттоманки, а, также, выкатной части дивана &ndash; деревянная рамка с амортизаторами из резино-тканевой ленты. В основании подголовной части &ndash; фанера. 
  <br />
 Корпус изделия сформирован настилочными материалами: эластичный пенополиуретан различной толщины, плотности и жесткости; синтепон; комфорель. 
  <br />
 Для наполнения спинки дивана, кресла и т.д. также используется комфорель. В этом случае для того, чтобы наполнение распределялось равномерно и не оседало, используются внутренние чехлы из хлопчатобумажной ткани.</ul>
 
<ul><a name="link6"></a><b>6. Кавказский бук (его характеристики)</b> 
  <br />
 Кавказский бук - одна из ценнейших пород дерева. Она наиболее подходит для наших климатических условий. Буковая древесина имеет мелкую структуру. Она твердая и прочная. Это продлевает срок службы мягкой мебели в несколько раз, по сравнению с другими породами дерева. 
  <br />
 Кавказский бук - удивительно красивое и многогранное дерево. Древесина его имеет желтоватый цвет с оттенками от розово-желтого до красновато-бурого. Текстурный рисунок, образованный линиями годичных слоев, меняется в зависимости от разреза. На поперечном срезе мы увидим концентрические окружности. На радиальном продольные полосы. А на тангенциальном (угловом) срезе тонкие, как будто нанесенные пером и тушью, конусообразные линии. Самым красивым является тангенциальный срез. 
  <br />
 Вся поставляемая нам древесина проходит контроль на соответствие технологическим требованиям. Для производства мебели используются только деревья с четкими годичными кольцами, крупными сосудами и сердцевидными лучами. Именно они в дальнейшем дадут красивый орнамент и цвет креслам и диванам. 
  <br />
 Окраску дереву придают красящие дубильные вещества, которые находятся в его клетчатке. А цвет древесины определяется спектральным составом отраженного деревом светового потока. Именно этим объясняется то, что под разным углом и интенсивностью освещения один и тот же диван может восприниматься человеком как совершенно разные по цвету диваны. Кроме того, на цвет и блеск древесины влияет месторождение дерева, ранняя или поздняя это древесина и т.д. 
  <br />
 Но именно эти небольшие различия в цвете и блеске, пятнышки от сучков, отклоненные неразвившимися побегами-глазками годичные слои делают каждый диван из натурального дерева оригинальным, неповторимым и только Вашим. Именно по этим особым приметам можно безошибочно отличить живую мебель от самой качественной высокотехнологичной подделки.</ul>
 
<ul><a name="link7"></a><b>7. Правила приемки мебели</b> 
  <br />
 Мебель соответствует ГОСТ 19917-93 (мебель для сидения и лежания), 16371-93. 
  <br />
 <b><font color="#ff0000">Внимание!</font></b> Мебель транспортируется в закрытых транспортных средствах и в соответствующей упаковке. При хранении мягкой мебели она должна быть надежно защищена от намокания, грязи, сильных толчков, ударов тяжелыми предметами, а, также, недопустимо падение с любой высоты. 
  <br />
 При доставке обязательно проверьте внешний вид и комплектность изделия, верность заполнения гарантийного талона. Все претензии к качеству исполнения заказа следует занести в договор. Помните, если Вы приняли изделие без указания на явные недостатки (недостатки, которые могут быть установлены при обычном способе приемки), это лишает Вас права ссылаться на них в дальнейшем. 
  <br />
 Допускаются и не являются дефектом: морщины на обивочном материале, возникающие после снятия нагрузок и исчезающие после разглаживания рукой; незначительная разнооттеночность облицовочных тканей; цвета и отличие рисунка лакокрасочных покрытий (декора); мелкие волосяные трещины (т.н. эффект растрескивания) и потертости мебельного покрытия из натуральной кожи, возникающие в местах интенсивной эксплуатации.</ul>
 
