<ul><a name="link2"></a><b>2. Уход за мебельными тканями</b> 
  <li>для удаления загрязнений &ndash; волокна отчистить с помощью щетки (избегайте использовать щетки с жесткими волокнами, так как они могут повредить ворс ткани). Вакуумная чистка &ndash; пылесос (следует использовать подходящее оборудование для вакуумной чистки), слишком часто использовать не следует, так как это негативно сказывается на качестве ткани, т.е. снижается устойчивость ворса, ворс выпадает и т.д.; </li>
 
  <li>использование воды должно быть ограничено. Излишняя влага может заставить пятно растечься и оставить на ткани разводы. Поэтому лучше использовать специальные средства, предназначенные для очистки мебельных тканей. Любые пятна и загрязнения нужно удалять сразу же, не допуская их высыхания на ткани; </li>
 
  <li>жидкости должны быть немедленно удалены с поверхности, чтобы предотвратить растекание и проникновение в основу ткани и ворса. Твердые материалы (такие, как свечной воск и парафин) необходимо раздробить, очистить вакуумным пылесосом, чтобы удалить вещество наиболее быстро; </li>
 
  <li>чтобы избежать появления разводов, составы для удаления пятен необходимо использовать в малых количествах и наносить их от границ пятна к центру; </li>
 
  <li>предварительно опробуйте действие чистящего средства на образце ткани или на невидном участке изделия; </li>
 
  <li>никогда не используйте совместно несколько различных пятновыводящих средств. </li>
 </ul>
 
<ul><a name="link3"></a><b>3. Уход за кожаной мебелью</b> 
  <li>никогда не применяйте для чистки кожаной мебели химические растворители, пятновыводители, другие агрессивные субстанции; </li>
 
  <li>никогда не используйте для чистки кожаной мебели абразивные материалы &ndash; порошки и пасты; </li>
 
  <li>удаляйте грязь с кожи с помощью воды и натурального мыла: намочите вату, протрите пятно круговыми движениями, высушите мокрое место сухой тканью; </li>
 
  <li>избегайте излишнего трения, протирайте кожаную мебель лёгкими движениями; </li>
 
  <li>располагайте кожаную мебель на расстоянии не менее 30-40 см от источника тепла. Избегайте попадания прямых солнечных лучей на кожаную мебель; </li>
 
  <li>регулярно стирайте пыль с кожаной мебели мягкой влажной тканью; </li>
 
  <li>никогда не используйте фен и обогревательные приборы для сушки кожаной мебели; </li>
 
  <li>не применяйте воду и растворители с целью уничтожить пятно растительного или животного жира. Протрите пятно сухой тканью и оставьте. Вскоре оно впитается в кожу и станет незаметным; </li>
 
  <li>пролитую жидкость на кожаной мебели немедленно промокните губкой или тканью, пропитанной тёплой мыльной водой; </li>
 
  <li>в условиях низких температур, после разгрузки мебель из кожи необходимо оставить в упакованном состоянии в тёплом помещении не менее чем на 24 часа. 
