<p>Способ трансформации:</p>

<p><span>1.<span>    </span></span>Потянуть за середину царги, выдвинуть на себя выкатную телегу (поз.3), которая приводит в движение сидение (поз.2) и вытягивает за собой подголовник (поз.1). Движение механизма происходит до полной фиксации;</p>

<p><span>2.<span>    </span></span>Трансформировать подушку выкатной телеги (поз.3) в верхнее положение за специальную ручку;</p>

<p><span>3.<span>    </span></span>Изделие разложено в положение «кровать».</p>

<p>Трансформация в положение «диван» производится в следующей последовательности:</p>

<p><span>1.<span>    </span></span>Трансформировать подушку выкатной телеги (поз.3) в нижнее положение;</p>

<p><span>2.<span>    </span></span>Задвинуть выкатную телегу до полного ухода подголовника (поз.1) вниз и установки сидения на место;</p>

<p><span>3.<span>    </span></span>Задвинуть окончательно выкатную телегу.</p>
