<p>В диванах, производимых на нашей фабрике, наряду с широко известными механизмами, применяются механизмы, разработанные в конструкторском отделе нашего предприятия. А именно:</p>

<p><b><u>&laquo;Книжка МС&raquo; </u></b>- современный механизм для ежедневного использования. Простой в эксплуатации. Позволяет формировать спальное место без перепадов по высоте. За счет трансформации этого механизма из дивана образуется кровать размером 1300х1950 мм.</p>

<img height="113" src="/about/engineering/knizhka.jpg" width="510" />

<p>Преимущество механизма состоит в том, что при раскладывании и складывании, диван не нужно отодвигать от стены </p>

<p align="center">Способ трансформации:</p>

<p>1. Вытянуть сиденья на себя.</p>

<p>2 .Приподнять сиденье до щелчка.</p>

<p>3. Опустить сиденье.</p>

<p>Трансформация в положение «диван» производится в обратной последовательности.</p>

<p><b> </b></p>

<p><b><u>«Авангард» ( «Конрад» )</u></b>- современный выкатной механизм для ежедневного использования. </p>

